// This class defines cars and its parameters
class Car {
  final String model;
  final String brand;
  final int builtYear;
  final String plate;
  final int modelYear;
  final String photo;
  final double pricePaid;
  final DateTime purchasedWhen;

  Car(this.model, this.brand, this.builtYear, this.plate, this.modelYear,
      this.photo, this.pricePaid, this.purchasedWhen);
}
